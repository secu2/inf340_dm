<h1>Bienvenue sur le site du Dahu !</h1>
<p>
	<h2> Qu'est ce que le site du dahu ?</h2>
	Le site du dahu est un site de partage d'impressions. Ici vous pourrez vous renseigner afin que votre prochaine sortie soit réussie, 
	ceci grâce à tous nos utilisateurs, et aussi grâce à vous qui donnent leur avis sur les différentes stations
	et postent des images de celle-ci.
	<br />
	Tout cela vous permettant donc, d'avoir une sortie de ski réussie la prochaine fois.
	
	
	<h2> Comment accéder à toutes ces informations ?</h2>
	Inscrivez-vous et retrouvez la liste des stations de ski de votre région, commentées 
	par nos membres.
</p>
